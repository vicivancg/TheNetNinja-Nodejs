//Installing nodemon

/*
    Nodemon permite monitorear los archivos y una vez que se ha realizado
    un cambio se refleja, sin tener que detener la ejecucíon de la app y
    volver a iniciarla, lo que cuando se esta desarrollando es muy utíl.

    se instala con el comando

    npm install -g nodemon

    el parametro -g significa que se instala globalmente, no solo en el
    proyecto.

    para utilizar nodemon, ejecutamos la app con:
    
    nodemon archivo.js

    cada que se haga un cambio, nodemon reiniciara node para que se
    reflejen los cambios.

*/