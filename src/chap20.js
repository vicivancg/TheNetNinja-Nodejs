//The node package manager NPM

/*NPM es un completo ecosistema que permite la administracion,
  acceso y publicación de packetes, haciendo facil el manejo
  de dependecias de los mismos. (Es genial !!)
*/

/*
    Podemos instalar por ejemplo express utilizando

    npm install express

    Con lo que se instalara express en el proyecto y todas sus dependencias
    y podamos utilizarlo comodamente (si yo también amo a NPM).

    También podemos eliminar paquetes, por ejemplo para eliminar express
    utilizamos:

    npm uninstall express

    la información de los paquetes y su instalación puede ser consultada
    desde la pagina de NPM.
*/